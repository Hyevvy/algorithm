#include <iostream>
#include <stdio.h>
using namespace std;
int tc;
int main() {
	int t1[23] = { 500, 300, 300, 200, 200,200,50,50,50,50,30,30,30,30,30,10,10,10,10,10,10 };
	int t2[33] = { 512, 256,256,128,128,128,128,64,64,64,64,64,64,64,64,32,32,32,32,32,32,32,32,32,32,32,32,32,32,32,32 };
	cin >> tc;
	int a, b;
	for (int i = 0; i < tc; i++) {

		cin >> a >> b;
		if (a >= 1 && a <= 21 && b >= 1 && b <= 31) {
			cout << t1[a - 1] + t2[b - 1] << "0000\n";
		}
		else if (a > 21 && (b >= 1 && b <= 31) || a == 0 && (b >= 1 && b <= 31)) {
			cout << t2[b - 1] << "0000\n";
		}
		else if (b > 31 && (a >= 1 && a <= 21) || b == 0 && (a >= 1 && a <= 21)) {
			cout << t1[a - 1] << "0000\n";
		}
		else {
			cout << "0\n";
		}
	}

	return 0;
}